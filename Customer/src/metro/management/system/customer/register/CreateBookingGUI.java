    private void filterBookings() {
        try {
            String nameFilter = jTextField3.getText();
            String dateFilter = jTextField1.getText();

            String query = "SELECT Name, Source, Destination, Time, Date FROM bookings WHERE Name LIKE ? AND Date LIKE ?";
            sss = sos.prepareStatement(query);
            sss.setString(1, "%" + nameFilter + "%");
            sss.setString(2, "%" + dateFilter + "%");
            rs = sss.executeQuery();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); 

            while (rs.next()) {
                String name = rs.getString("Name");
                String source = rs.getString("Source");
                String destination = rs.getString("Destination");
                String time = rs.getString("Time");
                String date = rs.getString("Date");

                model.addRow(new Object[]{name, source, destination, time, date});
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: Unable to fetch filtered bookings.", "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            
        }
    }
    
private void createBooking() {
    try {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Error: Please select a row to create a booking.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String name = jTable1.getValueAt(selectedRow, 0).toString();
        String source = jTable1.getValueAt(selectedRow, 1).toString();
        String destination = jTable1.getValueAt(selectedRow, 2).toString();
        String time = jTable1.getValueAt(selectedRow, 3).toString();
        String date = jTable1.getValueAt(selectedRow, 4).toString();

      
        int seats = Integer.parseInt(jTextField2.getText());

       
        String username = jTextField1.getText(); 
        String cnic = "SampleCNIC"; 

        // Insert into customer_bookings table
        String bookingQuery = "INSERT INTO customer_bookings (Name, Source, Destination, Time, Date, Username, CNIC, Seats) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement bookingStatement = sos.prepareStatement(bookingQuery);
        bookingStatement.setString(1, name);
        bookingStatement.setString(2, source);
        bookingStatement.setString(3, destination);
        bookingStatement.setString(4, time);
        bookingStatement.setString(5, date);
        bookingStatement.setString(6, username);
        bookingStatement.setString(7, cnic);
        bookingStatement.setInt(8, seats);
        bookingStatement.executeUpdate();

       
        String loginQuery = "UPDATE login_table SET balance = balance - ? WHERE username = ?";
        PreparedStatement loginStatement = sos.prepareStatement(loginQuery);
        loginStatement.setInt(1, seats);
        loginStatement.setString(2, username);
        loginStatement.executeUpdate();

       

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Error: Please enter a valid number for the number of seats.", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (SQLException ex) {
        // Handle SQL exception
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error: Failed to create a booking.", "Error", JOptionPane.ERROR_MESSAGE);
    } finally {
         JOptionPane.showMessageDialog(null, "Booking created!");
    }
}
    
