    private void populateBookingsTable() {
        try {
            String query = "SELECT Name, Source, Destination, Time, Date FROM bookings";
            sss = sos.prepareStatement(query);
            rs = sss.executeQuery();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Clear existing rows

            while (rs.next()) {
                String name = rs.getString("Name");
                String source = rs.getString("Source");
                String destination = rs.getString("Destination");
                String time = rs.getString("Time");
                String date = rs.getString("Date");

                model.addRow(new Object[]{name, source, destination, time, date});
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: Unable to fetch bookings.", "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                if (rs != null) rs.close();
                if (sss != null) sss.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }
